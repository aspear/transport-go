package util

import (
    "fmt"
    "strings"
)

func ExtractChannelNameFromDestination(dest string) (string, error) {
    if strings.Contains(dest, "/topic/", ) {
        return strings.ReplaceAll(dest, "/topic/", ""), nil
    }
    if strings.Contains(dest, "/queue/") {
        return strings.ReplaceAll(dest, "/queue/", ""), nil
    }
    return dest, fmt.Errorf("no channel name available, unknown destination type '%s'", dest)
}
